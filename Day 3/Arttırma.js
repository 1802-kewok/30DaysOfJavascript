// 1-Öncesi Artış

let count = 0;
console.log(++count);
console.log(count);

//2-Sonrası Artış
let deger = 0;
console.log(deger++); // 0
console.log(deger); // 1

/* Azaltma Operatörü */

// 1-Öncesi Azaltma
let Azaltma = 0;
console.log(--Azaltma); // -1
console.log(Azaltma); // -1

// 2-Sonrası Azaltma
let Sonrası = 0;
console.log(Sonrası--); // 0
console.log(Sonrası); // -1
